package Basic_java_program;

public class Variable_DataType {
    public static void main(String[] args) {
        int num = 1234;
        short a_short = 86;
        long along = 83047;
        double a_double = 9.5;
        float afloat = 8.7f;
        String name = "Welcome Ashok";


        char a_char ='a';
        boolean a_boolean = true;
        byte a_byte = 127;

        System.out.println("num:" + num);
        System.out.println("Short:" + a_short);
        System.out.println("Long:" + along);
        System.out.println("Double:" + a_double);
        System.out.println("Float:" + afloat);
        System.out.println("String:" + name);

        System.out.println("Char:" + a_char);
        System.out.println("Boolean:" + a_boolean);
        System.out.println("Byte:" + a_byte);



    }

}
