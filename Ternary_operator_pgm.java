package Basic_java_program;

public class Ternary_operator_pgm {
    public static void main(String[] args){
        System.out.println(true ? "true":"false");
        int a =25;
        int b =15;
        System.out.println(a+" > "+b +" = " +( a > b ? "true": "false"));
        System.out.println(a+" < "+b +" = " +(a < b ? "true":"false"));
        System.out.println(a+" == "+b +" = " +(a == b ? "true":"false"));

    }
}
